int	ft_intlen(int n)
{
	int	len;

	len = 1;
	if (n <= -2147483648)
		return (10);
	n *= (1 + -2 * (n < 0));
	while (n > 9)
	{
		n /= 10;
		len++;
	}
	return (len);
}
